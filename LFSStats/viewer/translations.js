// Current language
let currentLang = 'en';

// Detect browser language
function detectLanguage() {
    const browserLang = (navigator.language || navigator.userLanguage).split('-')[0];
    return TRANSLATIONS[browserLang] ? browserLang : 'en';
}

// Get translation
function t(key) {
    return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS['en'][key] || key;
}

// Translate status values
function translateStatus(status) {
    const statusMap = {
        'finished': 'statusFinished',
        'lapped': 'statusLapped',
        'dnf': 'statusDnf'
    };
    return t(statusMap[status] || status);
}

// Translate weather from JSON
function translateSessionLength(sessionLength) {
    if (!sessionLength) return sessionLength;
    const unitMap = {
        'Hours': { es: 'Horas', fr: 'Heures', pt: 'Horas', de: 'Stunden', it: 'Ore', pl: 'Godzin', ru: 'Часов', tr: 'Saat', fi: 'Tuntia', sv: 'Timmar', lt: 'Valandų', ja: '時間', zh: '小时', nl: 'Uur', da: 'Timer' },
        'Hour': { es: 'Hora', fr: 'Heure', pt: 'Hora', de: 'Stunde', it: 'Ora', pl: 'Godzina', ru: 'Час', tr: 'Saat', fi: 'Tunti', sv: 'Timme', lt: 'Valanda', ja: '時間', zh: '小时', nl: 'Uur', da: 'Time' },
        'Minutes': { es: 'Minutos', fr: 'Minutes', pt: 'Minutos', de: 'Minuten', it: 'Minuti', pl: 'Minut', ru: 'Минут', tr: 'Dakika', fi: 'Minuuttia', sv: 'Minuter', lt: 'Minučių', ja: '分', zh: '分钟', nl: 'Minuten', da: 'Minutter' },
        'Laps': { es: 'Vueltas', fr: 'Tours', pt: 'Voltas', de: 'Runden', it: 'Giri', pl: 'Okrążeń', ru: 'Кругов', tr: 'Tur', fi: 'Kierrosta', sv: 'Varv', lt: 'Ratų', ja: '周', zh: '圈', nl: 'Rondes', da: 'Omgange' }
    };
    for (const [en, translations] of Object.entries(unitMap)) {
        if (sessionLength.includes(en)) {
            const localized = translations[currentLang] || en;
            return sessionLength.replace(en, localized);
        }
    }
    return sessionLength;
}

function translateWeather(weather) {
    if (!weather) return weather;
    const weatherMap = {
        'Sunrise': 'sunrise',
        'Sunset': 'sunset',
        'Sunny': 'sunny',
        'Cloudy': 'cloudy',
        'Rainy': 'rainy',
        'no wind': 'noWind'
    };
    return t(weatherMap[weather] || weather);
}

